//A biblioteca Math serve para fazermos muitas coisa envolvendo números

//Mostrando o número pi
let pi = Math.PI
console.log(pi)

//Calcular a raiz quadrada
let raizQuadrada = Math.sqrt(16)
console.log(raizQuadrada)

//Calcular a potência
let potencia = Math.pow(2, 3)  //dois elevado a três
console.log(potencia)

let aleatorio = Math.round(Math.random() * 10)       //Math.random() gera um número aleatório de 0 a 0,99
console.log(aleatorio)                               //Math.round() arredonda o número que está dentro dele

